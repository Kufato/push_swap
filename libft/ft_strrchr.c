#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*buff;

	i = 0;
	buff = (char *)s;
	if ((char)c == '\0')
	{
		while (*buff)
			buff++;
		return (buff);
	}
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
